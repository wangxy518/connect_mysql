package bean;

public class NationSubjectTotalBean {

	private int id;
	private String subject;
	private String income;
	private String tongbiIncrease;
	private String month;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public String getIncome() {
		return income;
	}
	public void setIncome(String income) {
		this.income = income;
	}
	public String getTongbiIncrease() {
		return tongbiIncrease;
	}
	public void setTongbiIncrease(String tongbiIncrease) {
		this.tongbiIncrease = tongbiIncrease;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	
}
